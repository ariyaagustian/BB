				<br>
				&ensp;Dengan karakteristiknya yang merupakan semikonduktor yang sangat baik, germanium memiliki ion negatif untuk terus stabil terhadap arus listrik yang ada pada tubuh manusia . Hal ini karena ion negatif pada tubuh manusia yang mengandung listrik hampir menyerupai pada arus aliran air sungai yang mengalir, untuk menjaga kestabilan arus aliran listrik yang ada pada tubuh anda, anda tidah hanya berolahraga ataupun beristirahat secara teratur. Anda juga dapat memanfaatkan batu germanium untuk menjaga kestabilannya seperti manfaat teh bunga kamboja dan selain untuk menjaga kestabilan ion negatif dalam tubuh, adapun berbagai manfaat dari batu germanium.
				<br>
				Manfaat Batu Germanium <br>

				<b>1. Mengobati Sakit Kepala</b> <br>
				&ensp;Manfaat batu germanium yang pertama yaitu mengobati sakit kepala hampir sama dengan manfaat teh cap botol ataupun pusing. Kandungan ion yang terdapat pada batu germanium dapat mengurangi sakit pada kepala anda serta rasa pusing yang berlebih. Anda dapat mengompreskan batu germanium pada kepala anda yang sakit ataupun pusing.
				<br>
				<b>2. Meredakan Demam</b><br>
				&ensp;Tidak hanya mengobati sakit kepala, batu germanium dapat meredakan sakit demam yang anda derita. Batu germanium jika dikombinasikan dengan air maka suhu batu akan menurun menjadi dingin. Dengan suhu sedingin batu germanium, ini merupakan kompres yang sangat baik untuk dijadikan kompres penurun demam. Anda tidak perlu susah- susah lagi untuk membeli obat di apotek, cukup mengompreskan batu germanium di kepala anda maka panas ataupun demam pada tubuh anda akanhilang.
				<br>
				<b>3. Menenagkan Pikiran</b> <br>
				&ensp;Bagi anda yang sedang stress, anda dapat mengompreskan batu geranium atau meminum air rendaman batu germanium, sama dengan manfaat daun binahong maka stress yang anda alami akan hilang. Hal ini karena batu germanium dapat memicu hormon serotonin pada tubuh anda yang dapat mengurangi rasa stress ataupun jenuh.
				<br>
				<b>4. Mengobati Nyeri Bahu</b> <br>
				&ensp;Selain menggunakan alat pijit untuk meredakan nyeri bahu, anda juga dapat menggunakan batu germanium. Batu ini sangatlah efektif dijadikan sebagai alat pijat apalagi jika anda mengalami nyeri bahu, hal ini karena ion yang dimiliki oleh germanium dapat membuat otot- otot pada bahu anda menjadi normal kembali.
				<br>
				<b>5. Mengencangkan Kulit wajah</b> <br>
				&ensp;Selain bermanfaat untuk kesehatan, batu germanium juga sangat baik bagi perawatan kecantikan wajah. Selain menggunakan setlika wajah untuk mengencangkan wajah anda serta menghilangkan keriput pada wajah anda, anda dapat memakai batu germanium. Selain lebih praktis, ion negatif yang dipancarkan oleh batu germanium sangat baik tidak hanya kencang, maka wajah anda akan kelihatan lebih muda. Anda dapat memanfaatkan batu germanium setiap waktu, kapanpun anda mau. <br>
